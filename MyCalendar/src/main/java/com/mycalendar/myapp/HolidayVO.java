package com.mycalendar.myapp;

public class HolidayVO {
	int holMonth;
	int holDate;
	String holName;
	public int getHolMonth() {
		return holMonth;
	}
	public void setHolMonth(int holMonth) {
		this.holMonth = holMonth;
	}
	public int getHolDate() {
		return holDate;
	}
	public void setHolDate(int holDate) {
		this.holDate = holDate;
	}
	public String getHolName() {
		return holName;
	}
	public void setHolName(String holName) {
		this.holName = holName;
	}
}
